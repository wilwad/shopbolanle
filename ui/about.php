     <BR>

      <p><strong>Bolanle</strong>, which means <i>finds wealth at home</i> was founded by two young Namibians, who aim to improve the lives of Namibian artisans.</p>
      <p>All designs are fashioned in African timber and hand carved for unique individual effect. Items are made to clients’ specifications, ensuring uniqueness and value.</p> 
      <p>Paintings are done by the artist, and the outcome is dependent on the artist’s mood, contributing to each paintings uniqueness.</p>

    <p>All products are made on the African continent, using materials sourced locally. A percentage of the profits made are given to the artisans, and used in the further development and advancement of the working conditions and careers of the aforementioned people.</p>
    
    <BR>
